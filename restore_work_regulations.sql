  subtitle = 'الجودة والانضباط لضمان سلامة وسعادة أطفالنا',
  content = E'<div class="work-regulations-content">
<h2>المادة (1): مبادئ تنظيم الجدول (Scheduling)</h2>
<ul>
<li><strong>أساس الجدولة:</strong> يتم وضع الجداول الأسبوعية بواسطة الـ HR بناءً على احتياج العمل الفعلي (عدد الأطفال، الفترات، الفعاليات).</li>
<li><strong>أولوية الجدولة:</strong> تُمنح الأولوية في عدد الساعات واستقرار الجدول للشامبيونز (الخريجين / غير الطلاب) نظراً لمرونة إتاحتهم.</li>
<li><strong>تقديم الإتاحة:</strong> يجب تقديم الأيام والساعات (غير المتاحة) للأسبوع التالي في موعد أقصاه (الأربعاء) من كل أسبوع.</li>
<li><strong>أيام الذروة:</strong> (الاثنين، الخميس، الجمعة، العطلات، العروض) هي أيام ذروة. لا تٌقبل الأجازات فيها إلا للضرورة القصوى (مثل الامتحانات المثبتة بجدول).</li>
<li><strong>الالتزام بالجدول:</strong> الجدول الصادر نهائي. لا يمكن "تعويض" ساعات الأجازة في أيام أخرى لأن الجدولة تتم وفق احتياج محدد مسبقاً.</li>
<li><strong>تبديل الشيفتات:</strong> يُمنع الترتيب الشخصي للبدائل. أي طلب تبديل يجب أن يتم عبر الـ HR للموافقة.</li>
</ul>

<h2>المادة (2): معايير الأداء والسلوكيات (Performance & Conduct)</h2>
<ul>
<li><strong>سلامة الأطفال أولاً:</strong> بمجرد وجود طفل واحد، يُمنع منعاً باتاً:
<ul>
<li>استخدام الهاتف المحمول لأغراض شخصية.</li>
<li>التجمعات الجانبية بين الشامبيونز.</li>
<li>مغادرة منطقة العمل المخصصة بدون سبب أو إذن.</li>
</ul>
</li>
<li><strong>الاحترافية:</strong> يُمنع تماماً مناقشة أي أمور داخلية خاصة بالعمل أو "الهزار" غير اللائق أمام أولياء الأمور.</li>
<li><strong>اتباع التعليمات:</strong> الالتزام الفوري بتعليمات (الهوست أو الكابتن) أو الإدارة هو جزء أساسي من تقييم الأداء.</li>
</ul>

<h2>المادة (3): نظام الحوافز والعقوبات (Incentives & Consequences)</h2>
<ul>
<li><strong>مكافأة الإغلاق:</strong> سيتم تحديد مكافأة خاصة للشامبيون الملتزم بإنهاء ومتابعة مهام الشيفت المسائي كاملاً حتى الإغلاق.</li>
<li><strong>العقوبات (Consequences):</strong> عدم الالتزام (بالمادة 2) أو التأخير المتكرر سيؤدي مباشرة إلى:
<ul>
<li>تخفيض عدد الساعات المسندة في الجداول القادمة.</li>
<li>الاستبعاد الفوري من المهام ذات المسؤولية</li>
</ul>
</li>
</ul>

<h2>سياسات إضافية (التأخير والغياب)</h2>
<ul>
<li>يجب التواجد بالزي الرسمي قبل 5 دقائق من بدء الشيفت.</li>
<li>التأخير (حتى 15 دقيقة) يعرض الشامبيون للفت نظر وخصم ربع ساعة.</li>
<li>التأخير لأكثر من 15 دقيقة يُعتبر "غياباً" عن النصف الأول من الشيفت (يخصم ساعة كاملة).</li>
<li><strong>الغياب بعذر (مرض طارئ):</strong> يجب الإبلاغ الفوري للـ HR (قبل 3 ساعات على الأقل من الشيفت).</li>
<li><strong>الغياب بدون إذن:</strong> الغياب التام بدون إبلاغ يؤدي إلى (خصم يوم كامل) وعدم إسناد شيفتات في الأسبوع التالي.</li>
</ul>

<h2>سياسات إضافية (الزي والراحة)</h2>
<ul>
<li>الالتزام باليونيفورم نظيفاً وكاملاً.</li>
<li>ارتداء حذاء رياضي مريح ومغلق (للعمل الحركي).</li>
<li>يُمنع ارتداء الإكسسوارات الحادة أو الكبيرة التي قد تؤذي الأطفال.</li>
<li>للشامبيون العامل (6 ساعات متصلة) الحق في راحة (30 دقيقة).</li>
<li>يتم تحديد موعد الراحة بواسطة "الكابتن" لضمان عدم وجود فراغ.</li>
<li>تُقضى الراحة في المنطقة المخصصة للموظفين فقط.</li>
</ul>
</div>',
  is_active = true,
  updated_at = now()
WHERE organization_id IN (
  SELECT id FROM public.organizations
  WHERE name ILIKE '%gabalaya%' OR name ILIKE '%جبلاية%'
  LIMIT 1
);

